    }

    public static string RoundBytes(long num)
    {
      if (num < 1024L)
        return Conversions.ToString(num) + " bytes";
      if ((double) num < 1048576.0)
        return Conversions.ToString(Math.Round((double) num / 1024.0, 2)) + " KB";
      if ((double) num < 1073741824.0)
        return Conversions.ToString(Math.Round((double) num / 1048576.0, 2)) + " MB";
      if ((double) num < 1099511627776.0)
        return Conversions.ToString(Math.Round((double) num / 1073741824.0, 2)) + " GB";
      if ((double) num < 1.12589990684262E+15)
        return Conversions.ToString(Math.Round((double) num / 1099511627776.0, 2)) + " TB";
      return Conversions.ToString(num);
    }

