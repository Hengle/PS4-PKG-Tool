    public static bool IsAdmin()
    {
      bool flag = false;
      AppDomain.CurrentDomain.SetPrincipalPolicy(PrincipalPolicy.WindowsPrincipal);
      if (((WindowsPrincipal) Thread.CurrentPrincipal).IsInRole(WindowsBuiltInRole.Administrator))
        flag = true;
      return flag;
    }
